    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $messages = [
            'required' => ':Attribute is require',
            'email' => 'Fill :Attribute correctly',
        ];

        $request->validate([
            'name' => 'required',
            'email' => 'required|email',
            'subject' => 'required',
            'message' => 'required',
        ], $messages);

        // ELOQUENT
        $pesan = new Message;
        $pesan->name = $request->name;
        $pesan->email = $request->email;
        $pesan->subject = $request->subject;
        $pesan->message = $request->message;
        $pesan->save();

        Alert::success('Added Successfully', 'Your Message Added Successfully');

        return redirect()->route('contact.index');
    }

